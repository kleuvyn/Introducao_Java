public class TiposPrimitivos {
    public static void main(String[] args) {

        // Tipo int: para números inteiros
        int numeroInteiro = 10;
        System.out.println("Tipo int: " + numeroInteiro);

        // Tipo double: para números decimais
        double numeroDecimal = 3.14;
        System.out.println("Tipo double: " + numeroDecimal);

        // Tipo char: para caracteres individuais
        char caractere = 'A';
        System.out.println("Tipo char: " + caractere);

        // Tipo boolean: para valores verdadeiro (true) ou falso (false)
        boolean valorBooleano = true;
        System.out.println("Tipo boolean: " + valorBooleano);

        // Tipo byte: para números inteiros pequenos
        byte numeroByte = 100;
        System.out.println("Tipo byte: " + numeroByte);

        // Tipo short: para números inteiros um pouco maiores que o byte
        short numeroShort = 1000;
        System.out.println("Tipo short: " + numeroShort);

        // Tipo long: para números inteiros muito grandes
        long numeroLong = 1000000000L;
        System.out.println("Tipo long: " + numeroLong);

        // Tipo float: para números decimais de precisão simples
        float numeroFloat = 3.14f;
        System.out.println("Tipo float: " + numeroFloat);
    }
}
